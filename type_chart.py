# 辅助函数：计算倍率
def get_effectiveness(attacker, defender_types):
    multiplier = 1.0
    for d_type in defender_types:
        if attacker in TYPE_MAP and d_type in TYPE_MAP[attacker]:
            multiplier *= TYPE_MAP[attacker][d_type]
    return multiplier